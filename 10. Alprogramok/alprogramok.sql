CREATE OR REPLACE PROCEDURE mennyirerozsdamentes(csz INT)
IS
kesnev VARCHAR(30);
kesacel VARCHAR(30);
rozsdamentes NUMBER(20);
BEGIN
SELECT KES_BOLT.Gyarto||' '||KES_BOLT.Modell_Nev INTO  kesnev
FROM KES_BOLT
WHERE KES_BOLT.Cikkszam=csz;
SELECT  BLADEHQ_ACEL_ERTEKELES.Acel INTO kesacel
FROM BLADEHQ_ACEL_ERTEKELES
INNER JOIN KES_BOLT ON KES_BOLT.ACEL_ID=BLADEHQ_ACEL_ERTEKELES.Id
WHERE KES_BOLT.Cikkszam=csz;
SELECT  BLADEHQ_ACEL_ERTEKELES.Rozsdamentesseg INTO rozsdamentes
FROM BLADEHQ_ACEL_ERTEKELES
INNER JOIN KES_BOLT ON KES_BOLT.ACEL_ID=BLADEHQ_ACEL_ERTEKELES.Id
WHERE KES_BOLT.Cikkszam=csz;
DBMS_OUTPUT.PUT_LINE('Kés: '|| kesnev);
DBMS_OUTPUT.PUT_LINE('Acél: '|| kesacel);
DBMS_OUTPUT.PUT_LINE('Rozsdamentesseg 10/'|| rozsdamentes);
END;

EXECUTE mennyirerozsdamentes(1);

