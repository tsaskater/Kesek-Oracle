SET SERVEROUTPUT ON;

CREATE or REPLACE PROCEDURE keskereso(Csz INT)
IS
kesnev VARCHAR(30);
kesara NUMBER(20);
pengehossza DECIMAL(30);
BEGIN
SELECT KES_BOLT.Gyarto||' '||KES_BOLT.Modell_Nev INTO  kesnev
FROM KES_BOLT
WHERE KES_BOLT.Cikkszam=csz;
SELECT KES_BOLT.Ar INTO  kesara
FROM KES_BOLT
WHERE KES_BOLT.Cikkszam=csz;
SELECT KES_BOLT.Penge_hossz INTO  pengehossza
FROM KES_BOLT
WHERE KES_BOLT.Cikkszam=csz;
DBMS_OUTPUT.PUT_LINE('Kes: '|| kesnev);
DBMS_OUTPUT.PUT_LINE('Ar: '|| kesara);
DBMS_OUTPUT.PUT_LINE('Penge hossza: '|| pengehossza);
END;

EXECUTE keskereso(1);

