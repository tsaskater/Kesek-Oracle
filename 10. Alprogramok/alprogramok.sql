CREATE OR REPLACE PROCEDURE erossegvagas(csz INT)
IS
kesnev VARCHAR(30);
kesacel VARCHAR(30);
eltartasa NUMBER(30);
vagas NUMBER(30);
BEGIN
SELECT KES_BOLT.Gyarto||' '||KES_BOLT.Modell_Nev INTO  kesnev
FROM KES_BOLT
WHERE KES_BOLT.Cikkszam=csz;
SELECT  BLADEHQ_ACEL_ERTEKELES.Acel INTO kesacel
FROM BLADEHQ_ACEL_ERTEKELES
INNER JOIN KES_BOLT ON KES_BOLT.ACEL_ID=BLADEHQ_ACEL_ERTEKELES.Id
WHERE KES_BOLT.Cikkszam=csz;
SELECT  BLADEHQ_ACEL_ERTEKELES.Eltartas INTO eltartasa
FROM BLADEHQ_ACEL_ERTEKELES
INNER JOIN KES_BOLT ON KES_BOLT.ACEL_ID=BLADEHQ_ACEL_ERTEKELES.Id
WHERE KES_BOLT.Cikkszam=csz;
SELECT  CEDRIC_CUT_TESTS.Vagasok INTO vagas
FROM CEDRIC_CUT_TESTS
INNER JOIN KES_BOLT ON KES_BOLT.CEDRIC_ID=CEDRIC_CUT_TESTS.Id
WHERE KES_BOLT.Cikkszam=csz;

DBMS_OUTPUT.PUT_LINE('Kés: '|| kesnev);
DBMS_OUTPUT.PUT_LINE('Acél: '|| kesacel);
DBMS_OUTPUT.PUT_LINE('Éltartás 10/'|| eltartasa);
DBMS_OUTPUT.PUT_LINE('Vágások: '|| vagas);

END;

EXECUTE erossegvagas(1);

